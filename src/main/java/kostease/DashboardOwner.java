    private void tampilkanBeranda() {
        BerandaOwner bo = new BerandaOwner();
        bo.loadData(); 
        AddViews(bo);
    }

    private void AddViews(JPanel Dt) {
        if (form.getComponentCount() > 0) {
            form.removeAll();
        }
        form.add(Dt, BorderLayout.CENTER);
        form.revalidate();
        form.repaint();
    }

