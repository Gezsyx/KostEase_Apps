    private void editData() {
        try {
            String idKam = txtIdKam.getText();
            String mulai = txtMulai.getText();
            String akhir = txtAkhir.getText();
            String idPel = txtIdPel.getText().trim();

            Integer idPelanggan = null;
            Integer idkamar = Integer.parseInt(idKam);

            if (!idPel.isEmpty()) {
                idPelanggan = Integer.parseInt(idPel);
            }

            String Q = "UPDATE okupansi_kamar SET "
                    + "id_kamar=?, "
                    + "waktu_mulai=?, "
                    + "waktu_berakhir=?, "
                    + "id_pelanggan=? "
                    + "WHERE "
                    + "id_okupansi=?";

            Connection C = Koneksi.Go();
            PreparedStatement PS = C.prepareStatement(Q);
            PS.setInt(1, idkamar);
            PS.setString(2, mulai);
            PS.setString(3, akhir);
            if (idPelanggan != null) {
                PS.setInt(4, idPelanggan);
            } else {
                PS.setNull(4, java.sql.Types.INTEGER);
            }
            PS.setInt(5, Edok.getId());
            PS.executeUpdate();

            ManageOkupansi.refreshDataOkupansi("");
            this.setVisible(false);

            JOptionPane.showMessageDialog(null, "Berhasil merubah data");
        } catch (SQLException e) {
            System.err.println("Error:@simpanData() => " + e.getMessage());
        }
    }

